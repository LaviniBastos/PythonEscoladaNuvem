# Este código calcula e mostra em tela o preço total dos produtos comprados com base na quantidade comprada

produto = "cadeira infantil"
preco_unitario = 12.40
quant = 3

preco_total = preco_unitario * quant

print(f"O produto {produto} de valor R$ {preco_unitario:.2f}, foi comprado {quant} vezes, resultando em um total de: {preco_total:.2f}")