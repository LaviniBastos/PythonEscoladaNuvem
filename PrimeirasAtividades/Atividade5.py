# Calculando a diferença de um grupo de numero multiplicados: Multiplicamos A e B  fazemos a diferença com C e D

A = int(input("Digite um número: "))
B = int(input("Digite um número: "))
C = int(input("Digite um número: "))
D = int(input("Digite um número: "))

diferenca = (A * B) - (C * D)

print(f"A diferença dos numeros é {diferenca}" )