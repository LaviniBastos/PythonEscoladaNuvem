# atarefa é utilizar os operadores para criar uma calculadora com os operadores basicos
#soma, subtração, multiplicação e divisão

numero1 = 12
numero2 = 14

soma = numero1 + numero2
multi = numero1 * numero2
sub = numero1 - numero2
div = numero1 / numero2

print(f"a soma de {numero1} + {numero2} é igual a: {soma}")
print(f"a subtração de {numero1} + {numero2} é igual a: {multi}")
print(f"a subtração de {numero1} + {numero2} é igual a: {sub}")
print(f"a divisão de {numero1} + {numero2} é igual a: {div}")
print("A soma de {} + {} é {}".format(numero1, numero2, soma))